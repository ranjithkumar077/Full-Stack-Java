package LSM;
public class Address {
    String doorNumber;
    String street;
    String city;
    String state;
    String country;
    String pinCode;

    public Address(String doorNumber, String street, String city, String state, String country, String pinCode) {
        this.doorNumber = doorNumber;
        this.street = street;
        this.city = city;
        this.state = state;
        this.country = country;
        this.pinCode = pinCode;
    }
}

